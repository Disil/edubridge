    <p>Jurusan Psikologi adalah bidang ilmu yang mempelajari manusia secara mendalam, tidak hanya perilakunya, tetapi juga aspek jiwa yang mempengaruhi tindakan tersebut. Dalam konteks sosial, jurusan ini mengeksplorasi bagaimana manusia berinteraksi dengan lingkungannya, sementara dalam konteks industri, fokusnya adalah pada perilaku seseorang dalam hubungannya dengan posisinya di sebuah perusahaan.
