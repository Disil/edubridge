    <h1>Jurusan Teknik</h1>
    <h2>Deskripsi</h2>
    <p>Jurusan Teknik mencakup berbagai disiplin ilmu yang berfokus pada penerapan prinsip-prinsip ilmiah dan matematika untuk merancang, mengembangkan, dan memelihara sistem, struktur, dan teknologi. Bidang teknik sangat luas dan mencakup berbagai spesialisasi seperti Teknik Sipil, Teknik Mesin, Teknik Elektro, Teknik Kimia, Teknik Industri, Teknik Informatika, dan banyak lagi. Mahasiswa di jurusan teknik akan mempelajari teori dasar, keterampilan praktis, dan teknologi terbaru yang relevan dengan bidang mereka masing-masing.</p>
    <h2>Spesialisasi</h2>
    <p>Beberapa spesialisasi yang umum dalam jurusan teknik meliputi:</p>
    <ul>
        <li>Teknik Sipil: Fokus pada perancangan, konstruksi, dan pemeliharaan infrastruktur seperti jembatan, jalan, gedung, dan sistem air.</li>
        <li>Teknik Mesin: Merancang, mengembangkan, dan memelihara mesin dan sistem mekanik.</li>
        <li>Teknik Elektro: Merancang, mengembangkan, dan memelihara sistem listrik dan elektronik.</li>
        <li>Teknik Kimia: Menggabungkan prinsip-prinsip kimia dan biologi untuk merancang dan mengembangkan proses produksi bahan kimia, obat-obatan, makanan, dan bahan lainnya.</li>
        <li>Teknik Industri: Merancang, mengembangkan, dan memelihara sistem produksi dan manufaktur.</li>
        <li>Teknik Informatika: Berhubungan dengan desain, pengembangan, dan manajemen sistem perangkat keras dan perangkat lunak komputer</li>
    </ul>
    <h2>Prospek Kerja</h2>
    <h3>1. Industri dan Manufaktur</h3>
    <ul>
        <li>Engineer pabrik: Merancang, mengawasi, dan meningkatkan proses produksi di pabrik</li>
